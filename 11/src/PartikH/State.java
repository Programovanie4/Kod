    public void update() {
        System.out.println("update");
        int[][] playgroundCopy = new int[playground.length][playground[0].length];

        for(int i = 1; i < playground.length-1; i++){
            for(int j = 1; j < playground[i].length-1; j++) {
                int neighbours = neighbours(i, j);

                if (neighbours < 2 || neighbours > 3) {
                    playgroundCopy[i][j] = 0;
                } else if (playground[i][j] == 0 && neighbours == 3) {
                    playgroundCopy[i][j] = 1;
                } else {
                    playgroundCopy[i][j] = playground[i][j];
                }
            }
        }

        playground = Arrays.copyOfRange(playgroundCopy, 0, playgroundCopy.length);
    }

    public int neighbours(int x, int y) {
        int res = 0;
        for (int i = x-1; i <= x+1; i++) {
            for (int j = y-1; j <= y+1 ; j++) {
                if (i != x || j != y) {
                    if (i != playground.length && j != playground.length && i != 0 && j != 0) {
                        if (playground[i][j] == 1) res++;
                    }
                }
            }
        }
        return res;
    }

