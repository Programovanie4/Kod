public class Bod2D {  // v tomto module nic nepotrebuje riesit, takze ho nemente
	private double x, y;
	public Bod2D(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
}
